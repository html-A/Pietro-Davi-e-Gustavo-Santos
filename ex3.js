var N = parseInt(prompt ("Digite um numero referente a um dia do mês"));

if(N==1){
    alert("Hoje é Domingo");
}
if(N==2){
    alert("Hoje é Segunda");
}
if(N==3){
    alert("Hoje é Terça");
}
if(N==4){
    alert("Hoje é Quarta");
}
if(N==5){
    alert("Hoje é Quinta");
}
if(N==6){
    alert("Hoje é Sexta");
}
if(N==7){
    alert("Hoje é Sábado");
}